//program to demonstrate relational operations in java
class RelationalOperators
{
 public static void main(String ar[])
 {
 byte x=2;
 byte y=3;

 System.out.println("x==y:"+(x==y));
 System.out.println("x!=y:"+(x!=y));
 System.out.println("x>y:"+(x>y));
 System.out.println("x>=y:"+(x>=y));
 System.out.println("x<y:"+(x<y));
 System.out.println("x<=y:"+(x<=y));



 }
}